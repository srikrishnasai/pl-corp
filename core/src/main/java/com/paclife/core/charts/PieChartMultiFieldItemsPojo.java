package com.paclife.core.charts;

public class PieChartMultiFieldItemsPojo {
	private String barValue;
	private String barLabel;
	private String barColor;
	private String barWidth;

	public String getBarValue() {
		return barValue;
	}

	public void setBarValue(String barValue) {
		this.barValue = barValue;
	}

	public String getBarLabel() {
		return barLabel;
	}

	public void setBarLabel(String barLabel) {
		this.barLabel = barLabel;
	}

	public String getBarColor() {
		return barColor;
	}

	public void setBarColor(String barColor) {
		this.barColor = barColor;
	}

	public String getBarWidth() {
		return barWidth;
	}

	public void setBarWidth(String barWidth) {
		this.barWidth = barWidth;
	}

}
